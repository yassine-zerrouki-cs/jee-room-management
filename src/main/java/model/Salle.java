package model;

public class Salle {
	private int id;
	private String nom;
	private int Capacite;
	private int Bloc;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getCapacite() {
		return Capacite;
	}
	public void setCapacite(int capacite) {
		Capacite = capacite;
	}
	public int getBloc() {
		return Bloc;
	}
	public void setBloc(int bloc) {
		Bloc = bloc;
	}
	
	

}
